"""

------*       0 1   i=1 --> i+(-1)  
-----*-*      1 2   i=2 --> i+(-1)  8       j-n      n-j=-1 
----*---*     3 3   i=3 --> i+0     7       j-(n)    n-j= 0
---*******    5 4   i=4 --> i+1     6       j-(n)    n-j= 1
--*-------*   7 5   i=6 --> i+2     5       j-(n)    n-j= 2
-*---------*  9 6   i=6 --> i+3     4       j-(n)
*-----------*                      j-1      j-(n)

"""
n = 9
median = int((n/2))
j = n +1

for i in range(1,n+1):
    if i == (median+1):
        print(" "*(i-1)+"*"*(n))
        j-=1 
    elif i == 1:
        print(" "*(n-1)+"*")
    else:
        print(" "*(n-i)+"*"+" "*(i+n-j)+"*")
        j-=1
        
